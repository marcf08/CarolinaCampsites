map.on('mouseup', function(e) {
  if (e.originalEvent.button == 2) {
  let tempLat = e.lngLat.lat;
  let tempLong = e.lngLat.lng;
  $('#addToMapModal').modal('show');
  $('#resourceLatitude').val(tempLat);
  $('#resourceLongitude').val(tempLong);
  }
});

